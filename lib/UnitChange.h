#ifndef UNITCHANGE_H_
#define UNITCHANGE_H_

class UnitChange {
public:
	UnitChange() {};
	static double haeriToMeter(double haeri) { return haeri*1851; };
	static double meterToHaeri(double meter) { return meter/1851; };
	static double meterToKm(double meter) { return meter/1000; };
	static double kmToMeter(double km) { return km*1000; };
	static double feetToMeter(double feet) { return feet/3.28; };
	static double meterToFeet(double meter) { return meter*0.328; };
};
#endif /* UNITCHANGE_H_ */
